package Domain;

public class SizeObject {
	
	private int id;
	private String measure;
	
	
	public void setId(int id) {
		this.id=id;
	}
	
	public void setMeasure(String measure) {
		this.measure=measure;
	}
	
	
	public int getId() {
		return this.id;
	}
	
	public String getMeasure() {
		return this.measure;
	}
	

}
